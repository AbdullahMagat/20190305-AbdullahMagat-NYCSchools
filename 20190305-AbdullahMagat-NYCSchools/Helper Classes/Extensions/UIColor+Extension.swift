import Foundation
import UIKit
// MARK: - Special colors
extension UIColor {
    @nonobjc class var silver: UIColor {
        return UIColor(red: 190.0 / 255.0, green: 190.0 / 255.0, blue: 190.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var orangeRed: UIColor {
        return UIColor(red: 1.0, green: 75.0 / 255.0, blue: 26.0 / 255.0, alpha: 1.0)
    }
    
}
